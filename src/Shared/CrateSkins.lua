local ExportedData = {Raw = CrateSkins, Gun = {}, Claw = {}}

function ExportedData:GetAllTierSkins(skinType, rarity)
    return self.Shared.TableUtil.Filter(ExportedData[skinType],
                                        function(skin) return skin.Rarity == rarity end)
end

function ExportedData.GetSkinFromName(skinType, skinName)
    for _, skin in pairs(ExportedData[skinType]) do
        if skin.Name == skinName then
            return skin
        end
    end
end

for _, skin in pairs(CrateSkins) do
    if skin.Supports.Gun then
        ExportedData.Gun[#ExportedData.Gun + 1] = skin
    end

    if skin.Supports.Claw then
        ExportedData.Claw[#ExportedData.Claw + 1] = skin
    end
end

return ExportedData
